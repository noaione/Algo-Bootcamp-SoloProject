int main() {
    int exit_flag = 1, option = 0;
    while (exit_flag) {
        // put menu
        mainMenu();
        while (option < 1 || option > 9) {
            printf(">> ");
            scanf("%d", &option);
            getchar();
        }

        switch (option) {
        case 1:
            puts("");
            tambahMenuBaru();
            break;
        case 2:
            puts("");
            hapusMenu();
            break;
        case 3:
            puts("");
            buatPelangganBaru();
            break;
        case 4:
            puts("");
            cariDataPelanggan();
            break;
        case 5:
            puts("");
            lihatSeluruhDataPelanggan();
            break;
        case 6:
            puts("");
            buatOrderanBaru();
            break;
        case 7:
            puts("");
            lakukanPembayaran();
            break;
        case 8:
            // exit, don't forget to free the data :)
            freeDishesNode();
            freeCustomerNode();
            exit_flag = 0;
            break;
        }

        option = 0;
    }
    puts("");
    puts("Please expand your terminal to full screen!");
    puts("Press enter to continue...");
    getchar();
    // print splash-screen
    splashScreen();

    return 0;
}